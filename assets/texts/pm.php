			<a href="/ru/sc/?q=bu-pm#pli-tv-bu-pm:5.0"><h3>Pubbakaraṇaṁ</h3></a>
</div>
			
					  <div class="collapse" id="pkCollapse"> 
            <div class="my-3">
   
<div class="level4 my-3">
 <span class="level5"><strong>Pubbakaraṇaṁ</strong></br>
Sammajjanī padīpo ca, udakaṁ āsanena ca;
Uposathassa etāni, “pubbakaraṇa”nti vuccati. </span>
</div>     

<div class="level4 my-3">
 <span class="level5"><strong>Pubbakiccaṁ</strong></br>
Chanda, pārisuddhi, utukkhānaṁ, bhikkhugaṇanā ca ovādo;
Uposathassa etāni, “pubbakicca”nti vuccati.
</span>
</div>

<div class="level4 my-3">
 <span class="level5"><strong>Pattakallaaṅgā</strong></br>
Uposatho, yāvatikā ca bhikkhū kammappattā;
Sabhāgāpattiyo ca na vijjanti;
Vajjanīyā ca puggalā tasmiṁ na honti, “pattakalla”nti vuccati.
</span>
</div>

<div class="level4 my-3">
 <span class="level5">Pubbakaraṇapubbakiccāni samāpetvā desitāpattikassa samaggassa bhikkhusaṅghassa anumatiyā pātimokkhaṁ uddisituṁ ārādhanaṁ karoma. </span>
</div>
             
