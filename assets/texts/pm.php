			<a href="/ru/sc/?q=bu-pm#pli-tv-bu-pm:8.0"><h3>Pārājikuddeso</h3></a>
</div>
				  <div class="collapse" id="pjCollapse"> 
            <div class="mt-3">
   
           <div class="level4 my-3">
 <span class="level5">Tatrime cattāro pārājikā dhammā uddesaṁ āgacchanti. </span>
</div>              
              
<div class="level4">
 <span class="level5"><a href="/ru/sc/?q=bu-pm#pli-tv-bu-pm:9.0">Pārājika 1. Methunadhamma</a> <a href="/ru/sc/?q=bu-pj1#pli-tv-bu-vb-pj1:7.1.16.1">vb</a> Yo pana bhikkhu bhikkhūnaṁ sikkhāsājīvasamāpanno sikkhaṁ appaccakkhāya dubbalyaṁ anāvikatvā methunaṁ dhammaṁ paṭiseveyya, antamaso tiracchānagatāyapi, pārājiko hoti asaṁvāso. </span>
</div>    
 
<div class="level4">
 <span class="level5"><a href="/ru/sc/?q=bu-pm#pli-tv-bu-pm:10.0">Pārājika 2. Adinnādāna</a> <a href="/ru/sc/?q=bu-pj2#pli-tv-bu-vb-pj2:2.28.1">vb</a> 
Yo pana bhikkhu gāmā vā araññā vā adinnaṁ theyyasaṅkhātaṁ ādiyeyya,
yathārūpe adinnādāne rājāno coraṁ gahetvā haneyyuṁ vā bandheyyuṁ vā pabbājeyyuṁ vā corosi bālosi mūḷhosi thenosīti, tathārūpaṁ bhikkhu adinnaṁ ādiyamāno ayampi pārājiko hoti asaṁvāso. </span>
</div>     
 
 <div class="level4">
 <span class="level5"><a href="/ru/sc/?q=bu-pm#pli-tv-bu-pm:11.0">Pārājika 3. Manussaviggaha</a> <a href="/ru/sc/?q=bu-pj3#pli-tv-bu-vb-pj3:2.49.1">vb</a> 
Yo pana bhikkhu sañcicca manussaviggahaṁ jīvitā voropeyya, satthahārakaṁ vāssa pariyeseyya, maraṇavaṇṇaṁ vā saṁvaṇṇeyya, maraṇāya vā samādapeyya
“ambho purisa kiṁ tuyhiminā pāpakena dujjīvitena, mataṁ te jīvitā seyyo”ti,
iti cittamano cittasaṅkappo anekapariyāyena maraṇavaṇṇaṁ vā saṁvaṇṇeyya, maraṇāya vā samādapeyya, ayampi pārājiko hoti asaṁvāso. </span>
</div>    

<div class="level4">
 <span class="level5"><a href="/ru/sc/?q=bu-pm#pli-tv-bu-pm:12.0">Pārājika 4. Uttarimanussadhamma</a> <a href="/ru/sc/?q=bu-pj4#pli-tv-bu-vb-pj4:2.12.1">vb</a> Yo pana bhikkhu anabhijānaṁ uttarimanussadhammaṁ attupanāyikaṁ alamariyañāṇadassanaṁ samudācareyya “iti jānāmi, iti passāmī”ti,
tato aparena samayena samanuggāhīyamāno vā asamanuggāhīyamāno vā āpanno visuddhāpekkho evaṁ vadeyya “ajānamevaṁ āvuso avacaṁ jānāmi, apassaṁ passāmi, tucchaṁ musā vilapi”nti, aññatra adhimānā, ayampi pārājiko hoti asaṁvāso.
	 </span>
</div>    

           <div class="level4 my-3">
 <span class="level5">Uddiṭṭhā kho āyasmanto cattāro pārājikā dhammā.
Yesaṁ bhikkhu aññataraṁ vā aññataraṁ vā āpajjitvā na labhati bhikkhūhi saddhiṁ saṁvāsaṁ
yathā pure, tathā pacchā, pārājiko hoti asaṁvāso.
Tatthāyasmante pucchāmi, kaccittha parisuddhā,
dutiyampi pucchāmi, kaccittha parisuddhā,
tatiyampi pucchāmi, kaccittha parisuddhā,
parisuddhetthāyasmanto, tasmā tuṇhī, evametaṁ dhārayāmīti.
Pārājikaṁ niṭṭhitaṁ </span>
